#ifndef __KF_MUTEX_H__
#define __KF_MUTEX_H__

namespace KFrame
{
	///////////////////////////////////////////////////////////////////////
	class KFNullMutex
	{
	public:
		KFNullMutex() {}
		~KFNullMutex() {}

		void Lock() {}
		void Unlock() {}
	};

	///////////////////////////////////////////////////////////////////////////////////////
	class KFMutex
	{
	public:
		KFMutex();
		~KFMutex();

		void Lock();
		void Unlock();

	private:
		char _buff[ 128 ];
	};
	///////////////////////////////////////////////////////////////////////////////////////
	template< class T >
	class KFLocker
	{
	public:
		KFLocker( T& kfmutex ) : _kf_mutex( kfmutex )
		{
			_kf_mutex.Lock();
		}

		~KFLocker()
		{
			_kf_mutex.Unlock();
		}

	private:
		T &_kf_mutex;
	};
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////

}

#endif
