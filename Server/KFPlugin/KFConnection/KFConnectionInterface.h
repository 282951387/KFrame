#ifndef __KF_CONNECTION_INTERFACE_H__
#define __KF_CONNECTION_INTERFACE_H__

#include "KFrame.h"

namespace KFrame
{
    /////////////////////////////////////////////////////////////////////////////////////////////
    class KFConnectionInterface : public KFModule
    {
    public:
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // 监听端口
        virtual uint32 GetListenPort() const = 0;

        // 平台访问地址
        virtual const std::string& FindPlatformAddress( uint32 id ) = 0;

        // Master连接地址
        virtual const KFConnection* FindMasterConnection( const std::string& name ) = 0;

        // 修改mastr连接id
        virtual void SetMasterConnection( const std::string& name, uint32 zoneid ) = 0;
    };

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    __KF_INTERFACE__( _kf_connection, KFConnectionInterface );
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}



#endif